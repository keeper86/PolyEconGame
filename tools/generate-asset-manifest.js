#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const PUBLIC_DIR = path.join(__dirname, '..', 'public');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'lib', 'assetManifest.ts');

function scanDirectory(dir, basePath = '') {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const result = {};

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    const relativePath = basePath ? `${basePath}/${entry.name}` : entry.name;

    if (entry.isDirectory()) {
      const subResult = scanDirectory(fullPath, relativePath);
      Object.assign(result, subResult);
    } else if (entry.isFile()) {
      // Remove file extension and convert to key format
      const key = relativePath
        .replace(/\.(webp|png|jpg|jpeg|svg|gif)$/i, '')
        .toLowerCase()
        .replace(/\//g, '_')
        .replace(/-/g, '_');
      
      // Store the path relative to public directory with /images/ prefix
      // Since we're scanning from public/images, we need to add /images/ to the path
      result[key] = `/images/${relativePath}`;
      
      // Also add a key without the directory prefix for easier lookup
      // e.g., "aluminum" in addition to "products_aluminum"
      const fileName = path.basename(relativePath, path.extname(relativePath));
      const simpleKey = fileName.toLowerCase().replace(/-/g, '_');
      if (simpleKey !== key) {
        result[simpleKey] = `/images/${relativePath}`;
      }
    }
  }

  return result;
}

function generateManifest() {
  console.log('Scanning public directory for assets...');
  
  const imagesDir = path.join(PUBLIC_DIR, 'images');
  if (!fs.existsSync(imagesDir)) {
    console.error('Images directory not found:', imagesDir);
    process.exit(1);
  }

  const manifest = scanDirectory(imagesDir);
  
  // Sort keys alphabetically for better readability
  const sortedManifest = {};
  Object.keys(manifest).sort().forEach(key => {
    sortedManifest[key] = manifest[key];
  });

  const fileContent = `// This file is auto-generated by scripts/generate-asset-manifest.js
// Do not edit manually

export const assetManifest = ${JSON.stringify(sortedManifest, null, 2)} as const;

export type AssetKey = keyof typeof assetManifest;

export function getAssetPath(key: string): string {
  const normalizedKey = key.toLowerCase().replace(/\\s+/g, '_').replace(/-/g, '_');
  return assetManifest[normalizedKey as AssetKey] || '/images/products/question_mark.webp';
}
`;

  // Ensure the output directory exists
  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_FILE, fileContent);
  console.log(`Asset manifest generated: ${OUTPUT_FILE}`);
  console.log(`Found ${Object.keys(sortedManifest).length} assets`);
}

generateManifest();